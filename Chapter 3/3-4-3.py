# Недавно мы считали для каждого слова количество его вхождений в строку. Но на все слова может быть 
# не так интересно смотреть, как, например, на наиболее часто используемые.
# Напишите программу, которая считывает текст из файла (в файле может быть больше одной строки) и 
# выводит самое частое слово в этом тексте и через пробел то, сколько раз оно встретилось. 
# Если таких слов несколько, вывести лексикографически первое (можно использовать оператор < для строк).
# Слова, написанные в разных регистрах, считаются одинаковыми.
with open('C:\dataset_3363_4.txt') as inf, open('C:\Words.txt','w') as ouf:
    maxc = 0
    s = inf.read().lower().strip().split()
    s.sort()
    for word in s:
        counter = s.count(word)
        if counter > maxc:
            maxc = counter
            result_word = word
    ouf.write(result_word +' ' + str(maxc))